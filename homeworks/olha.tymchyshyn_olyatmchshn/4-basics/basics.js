const a = 24 + 68;
const b = 37 - 16;
const c = 100 / 5;
const x = 4 * 16;
const y = 4 ** 2;
const z = 23 % 5;

console.log(`Addition: ${a} + ${b} = ${a + b}`);

console.log(`Subtraction: ${b} - ${c} = ${b - c}`);

console.log(`Multiplication: ${y} * ${z} = ${y * z}`);

console.log(`Division: ${c} / ${z} = ${(c / z).toFixed(2)}`);

console.log(`Exponentiation: ${c} ** ${z} = ${c ** z}`);

console.log(`Remainder: ${x} % ${b} = ${x % b}`);

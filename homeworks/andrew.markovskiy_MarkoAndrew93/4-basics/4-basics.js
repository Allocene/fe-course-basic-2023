const a = 5;
const b = 4;

// Addition

const sum = a + b;
console.log(`Operation addition: ${a} + ${b} = ${sum}`);

//  Subtraction

const sub = a - b;
console.log(`Operation subtraction: ${a} - ${b} = ${sub}`);

//   Multiplication

const mult = a * b;
console.log(`Operation multiplication: ${a} * ${b} = ${mult}`);

//   Division

const div = b / a;
console.log(`Operation division: ${a} / ${b} = ${div}`);

// Receiving the balance

const receiving = a % b;
console.log(`Operation Receiving the balance): ${a} % ${b} = ${receiving}`);

// Exponentiation

const expon = a ** b;
console.log(`Operation exponentiation): ${a} ** ${b} = ${expon}`);
